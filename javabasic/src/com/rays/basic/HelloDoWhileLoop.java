package com.rays.basic;

public class HelloDoWhileLoop {

	public static void main(String[] args) {

		int i = 3;
		

		do {
			System.out.println(i);
		} while (i != 3);
	}

}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                         